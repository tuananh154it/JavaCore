public class bai2 {
    public static void main(String[] args) {
        double BanKinh = 13.5 ;
        double ChuVi = 2 * BanKinh * Math.PI ;
        double DienTich = BanKinh * BanKinh * Math.PI ;
        System.out.println("Chu vi hinh chu nhat ban kinh " + BanKinh + " la: " + ChuVi);
        System.out.println("Dien tich hinh chu nhat ban kinh " + BanKinh + " la: " + DienTich);
    }
}
